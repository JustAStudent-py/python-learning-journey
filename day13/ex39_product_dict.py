# Create a dictionary with product information
product = {
    "name": "Keyboard",  # key "name" with value "Keyboard"
    "price": 45,         # key "price" with value 45
}

# Add a new key "stock" with value "Available"
product["stock"] = "Available"

# Print the updated dictionary
print(product)  # Output: {'name': 'Keyboard', 'price': 45, 'stock': 'Available'}