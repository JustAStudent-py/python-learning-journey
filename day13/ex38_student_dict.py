# Create a dictionary with student information
student = {
    "name": "Elias",     # key "name" with value "Elias"
    "age": 24,           # key "age" with value 24
    "course": "Python"   # key "course" with value "Python"
}

# Access the value of the key "name"
print(student["name"])  # Output: Elias

# Access the value of the key "age"
print(student["age"])   # Output: 24